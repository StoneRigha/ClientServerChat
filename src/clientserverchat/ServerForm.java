    private void sendMessage() {
        if (dos == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Not connected to client yet.", "Connection Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        String msgout = jTextField1.getText().trim();
        if (msgout.isEmpty()) {
            return;
        }

        try {
            dos.writeUTF(msgout);
            String escapedMsg = msgout.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace("\"", "&quot;").replace("\n", "<br>");
            chatHTML.append("<div class='timestamp'>").append(LocalDateTime.now().format(formatter)).append("</div><div class='message-container sent-container'><div class='avatar'>Y</div><div class='sent'>").append(escapedMsg).append("</div></div><br clear='both'>");
            chatPane.setText(chatHTML.toString() + "</body></html>");
            try { chatPane.setCaretPosition(chatPane.getDocument().getLength()); } catch (Exception e) {}
            jTextField1.setText("");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Unable to send message: " + e.getMessage(), "Send Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

