    private void sendFileAction(java.awt.event.ActionEvent evt) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select File to Send");
        int result = fileChooser.showOpenDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            new Thread(() -> sendFile(selectedFile)).start();
        }
    }

    private void sendFile(File file) {
        try {
            byte[] fileData = Files.readAllBytes(file.toPath());
            synchronized(dos) {
                dos.writeUTF("FILE:" + file.getName() + ":" + fileData.length);
                dos.flush();
                dos.write(fileData);
                dos.flush();
            }
            
            String msg = "📎 Sent file: " + file.getName() + " (" + formatFileSize(file.length()) + ")";
            String escapedMsg = msg.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;");
            chatHTML.append("<div class='timestamp'>").append(LocalDateTime.now().format(formatter)).append("</div><div class='message-container sent-container'><div class='avatar'>Y</div><div class='sent'>").append(escapedMsg).append("</div></div><br clear='both'>");
            
            javax.swing.SwingUtilities.invokeLater(() -> {
                chatPane.setText(chatHTML.toString() + "</body></html>");
                try { chatPane.setCaretPosition(chatPane.getDocument().getLength()); } catch (Exception e) {}
            });
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error sending file: " + e.getMessage(), "File Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private void playNotificationSound() {
        try {
            AudioFormat audioFormat = new AudioFormat(8000, 8, 1, true, false);
            DataLine.Info info = new DataLine.Info(SourceDataLine.class, audioFormat);
            SourceDataLine line = (SourceDataLine) AudioSystem.getLine(info);
            line.open(audioFormat);
            line.start();
            
            int duration = 200;
            int samples = (int)(audioFormat.getSampleRate() * duration / 1000);
            byte[] soundData = new byte[samples];
            
            for (int i = 0; i < samples; i++) {
                soundData[i] = (byte)(Math.sin(2 * Math.PI * 800 * i / audioFormat.getSampleRate()) * 100);
            }
            
            line.write(soundData, 0, soundData.length);
            line.drain();
            line.close();
        } catch (Exception e) {
            // Silent fail for sound
        }
    }

    private void handleIncomingFile(String fileHeader) {
        try {
            String[] parts = fileHeader.split(":");
            String fileName = parts[1];
            long fileSize = Long.parseLong(parts[2]);
            
            byte[] fileData = new byte[(int)fileSize];
            int bytesRead = 0;
            while (bytesRead < fileSize) {
                int read = DI.read(fileData, bytesRead, (int)(fileSize - bytesRead));
                if (read == -1) break;
                bytesRead += read;
            }
            
            String downloadDir = System.getProperty("user.home") + File.separator + "Downloads";
            File downloadFile = new File(downloadDir, fileName);
            Files.write(downloadFile.toPath(), fileData);
            
            playNotificationSound();
            
            String msg = "📎 Received file: " + fileName + " (" + formatFileSize(fileSize) + ") - Saved to Downloads";
            String escapedMsg = msg.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;");
            chatHTML.append("<div class='timestamp'>").append(LocalDateTime.now().format(formatter)).append("</div><div class='message-container received-container'><div class='avatar'>C</div><div class='received'>").append(escapedMsg).append("</div></div><br clear='both'>");
        } catch (Exception e) {
            javax.swing.SwingUtilities.invokeLater(() -> 
                JOptionPane.showMessageDialog(null, "Error receiving file: " + e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE)
            );
        }
    }

    private String formatFileSize(long bytes) {
        if (bytes < 1024) return bytes + " B";
        int units = 1024;
        if (bytes < units * 1024) return String.format("%.1f KB", bytes / (double) units);
        if (bytes < units * units * 1024) return String.format("%.1f MB", bytes / (double) (units * units));
        return String.format("%.1f GB", bytes / (double) (units * units * units));
    }

